# OPS_REMOTE_STATUS_PUSH_SOURCE=${project_slug}
# OPS_REMOTE_SUMMARY_PUSH_ENABLED=1
# OPS_REMOTE_SUMMARY_PUSH_URL=
# OPS_REMOTE_SUMMARY_PUSH_SECRET=
# OPS_REMOTE_SUMMARY_PUSH_TIMEOUT_SEC=8
# OPS_REMOTE_SUMMARY_PUSH_SOURCE=${project_slug}
# OPS_REMOTE_SUMMARY_PUSH_HOURS=6
# OPS_REMOTE_SUMMARY_PUSH_MIN_INTERVAL_SEC=300
EOF
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --project)
      [[ $# -ge 2 ]] || { echo "Missing value for --project" >&2; exit 1; }
      project="$2"
      shift 2
      ;;
    --source-profile)
      [[ $# -ge 2 ]] || { echo "Missing value for --source-profile" >&2; exit 1; }
      source_profile="$2"
      shift 2
      ;;
    --output)
      [[ $# -ge 2 ]] || { echo "Missing value for --output" >&2; exit 1; }
      output_path="$2"
      shift 2
      ;;
    --force)
      force="1"
      shift
      ;;
    -h|--help|help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown option: $1" >&2
      usage
      exit 1
      ;;
  esac
done

if [[ -z "$project" ]]; then
  echo "Option --project is required" >&2
  usage
  exit 1
fi

project_slug="$(slugify "$project")"

if [[ -e "$output_path" && "$force" != "1" ]]; then
  echo "Output archive already exists: ${output_path}" >&2
  echo "Use --force to overwrite it." >&2
  exit 1
fi

source_env_file="$(resolve_source_env_file "$project_slug" || true)"
if [[ -n "$source_env_file" ]]; then
  export DAEMON_GH_ENV_FILE="$source_env_file"
fi

flow_base_branch="$(resolve_value "FLOW_BASE_BRANCH" "main")"
flow_head_branch="$(resolve_value "FLOW_HEAD_BRANCH" "development")"
auth_bind="$(resolve_value "GH_APP_BIND" "127.0.0.1")"
auth_port="$(resolve_value "GH_APP_PORT" "8787")"
fallback_enabled="$(resolve_value "DAEMON_GH_TOKEN_FALLBACK_ENABLED" "")"
if [[ -z "$fallback_enabled" ]]; then
  fallback_enabled="$(resolve_value "CODEX_GH_TOKEN_FALLBACK_ENABLED" "0")"
fi
auth_timeout="$(resolve_value "DAEMON_GH_AUTH_TIMEOUT_SEC" "8")"
auth_token_url="$(resolve_value "DAEMON_GH_AUTH_TOKEN_URL" "")"
launchd_namespace="$(resolve_value "FLOW_LAUNCHD_NAMESPACE" "com.flow")"

watchdog_interval="45"
if [[ -n "$source_env_file" && -f "$source_env_file" ]]; then
  watchdog_interval="$(codex_read_key_from_env_file "$source_env_file" "WATCHDOG_DAEMON_INTERVAL_SEC" || true)"
fi
[[ -n "$watchdog_interval" ]] || watchdog_interval="45"

build_dir="$(mktemp -d "${TMPDIR:-/tmp}/codex_migration_kit.XXXXXX")"
cleanup() {
  rm -rf "$build_dir"
}
trap cleanup EXIT

mkdir -p \
  "${build_dir}/.flow/config/profiles" \
  "${build_dir}/.flow/config/root" \
  "${build_dir}/.flow/launchd" \
  "${build_dir}/.flow/state/codex/${project_slug}" \
  "${build_dir}/.flow/tmp" \
  "${build_dir}/.flow/templates/github"

cp -R "${ROOT_DIR}/.flow/scripts" "${build_dir}/.flow/"
cp -R "${ROOT_DIR}/.flow/docs" "${build_dir}/.flow/"
if [[ -f "${ROOT_DIR}/COMMAND_TEMPLATES.md" ]]; then
  cp "${ROOT_DIR}/COMMAND_TEMPLATES.md" "${build_dir}/COMMAND_TEMPLATES.md"
fi
if [[ -d "${ROOT_DIR}/.github/workflows" ]]; then
  mkdir -p "${build_dir}/.flow/templates/github/workflows"
  cp -R "${ROOT_DIR}/.github/workflows/." "${build_dir}/.flow/templates/github/workflows/"
fi
if [[ -f "${ROOT_DIR}/.github/pull_request_template.md" ]]; then
  cp "${ROOT_DIR}/.github/pull_request_template.md" "${build_dir}/.flow/templates/github/pull_request_template.md"
fi

touch "${build_dir}/.flow/state/codex/${project_slug}/.keep"
touch "${build_dir}/.flow/launchd/.keep"
write_profile_sample_env \
  "${build_dir}/.flow/config/profiles/${project_slug}.sample.env" \
  "$project_slug" \
  "$flow_base_branch" \
  "$flow_head_branch" \
  "$auth_bind" \
  "$auth_port" \
  "$fallback_enabled" \
  "$watchdog_interval" \
  "$launchd_namespace"
write_root_codex_env "${build_dir}/.flow/config/root/.env.codex" "$project_slug" "$auth_bind" "$auth_port" "$auth_timeout" "$auth_token_url" "$launchd_namespace"
write_repo_actions_manifest \
  "${build_dir}/.flow/config/root/github-actions.required-files.txt" \
  "${build_dir}/.flow/config/root/github-actions.required-secrets.txt"

cat > "${build_dir}/.flow/tmp/migration_kit_manifest.env" <<EOF
MIGRATION_KIT_PROJECT=${project_slug}
MIGRATION_KIT_CREATED_AT_UTC=$(date -u '+%Y-%m-%dT%H:%M:%SZ')
MIGRATION_KIT_SOURCE_ENV_FILE=$(basename "${source_env_file:-}")
MIGRATION_KIT_PROFILE_SAMPLE_ENV=.flow/config/profiles/${project_slug}.sample.env
MIGRATION_KIT_PROFILE_ENV_TARGET=.flow/config/profiles/${project_slug}.env
MIGRATION_KIT_CODEX_ENV=.flow/config/root/.env.codex
MIGRATION_KIT_STATE_DIR=.flow/state/codex/${project_slug}
MIGRATION_KIT_DOC_DIR=.flow/docs
MIGRATION_KIT_COMMAND_TEMPLATES=COMMAND_TEMPLATES.md
MIGRATION_KIT_REPO_ACTIONS_TEMPLATE=.flow/templates/github
MIGRATION_KIT_REPO_ACTIONS_FILES=.flow/config/root/github-actions.required-files.txt
MIGRATION_KIT_REPO_ACTIONS_SECRETS=.flow/config/root/github-actions.required-secrets.txt
EOF

mkdir -p "$(dirname "$output_path")"
rm -f "$output_path"
tar -czf "$output_path" -C "$build_dir" .

echo "MIGRATION_KIT_CREATED=${output_path}"
echo "MIGRATION_KIT_PROJECT=${project_slug}"
if [[ -n "$source_env_file" ]]; then
  echo "MIGRATION_KIT_SOURCE_ENV=${source_env_file}"
fi
echo "MIGRATION_KIT_PROFILE_SAMPLE=.flow/config/profiles/${project_slug}.sample.env"
echo "MIGRATION_KIT_CODEX_ENV=.flow/config/root/.env.codex"
echo "MIGRATION_KIT_REPO_ACTIONS=.flow/templates/github"
echo "MIGRATION_KIT_REPO_ACTIONS_SECRETS=.flow/config/root/github-actions.required-secrets.txt"
